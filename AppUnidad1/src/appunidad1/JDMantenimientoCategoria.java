    private void limpiarControles(){
        txtCodigo.setText("");
        txtNombre.setText("");
        txtDescripcion.setText("");
        chkVigencia.setSelected(false);
        deshabilitarEd();
    }
    
    private void listarCategorias(){
        ResultSet rsCategorias = null;
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("CÓDIGO");
        modelo.addColumn("NOMBRE");
        modelo.addColumn("DESCRIPCIÓN");
        modelo.addColumn("VIGENCIA");
        try {
            rsCategorias=objCategoria.listarCategorias();
            while(rsCategorias.next()){
                modelo.addRow(new Object[]{rsCategorias.getInt("codcategoria"),rsCategorias.getString("nomcategoria"), rsCategorias.getString("descripcion"), rsCategorias.getBoolean("vigencia")?"Vigente":"No Vigente"});
           }
        } catch (Exception e) {
        }
        tblCategorias.setModel(modelo);
    }
    
    private void deshabilitarEd(){
        btnModificar.setEnabled(false);
        btnEliminar.setEnabled(false);
        btnDardeBaja.setEnabled(false);
    }
    
    private void habilitarEd(){
        btnModificar.setEnabled(true);
        btnEliminar.setEnabled(true);
        btnDardeBaja.setEnabled(true);
    }
    
    private boolean validarDatos(){
        boolean valido = false;
        if(txtCodigo.getText().replace(" ", "").length()!=0 && txtNombre.getText().replace(" ", "").length()!=0){
            valido = true;
        }
        
        return valido;
    }
