    private void btnBuscarVentasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarVentasActionPerformed
        if(  dpFechaInicio.getDate() == null|| dpFechaFin.getDate()==null){
            JOptionPane.showMessageDialog(this, "Por favor llene los campos necesarios, si solo desea buscar por una fecha puede ir a la interfaz ventas diarias");
        }else{
            Date finicio = Date.valueOf(dpFechaInicio.getDate());
            Date ffin = Date.valueOf(dpFechaFin.getDate());
            listarVentas(finicio, ffin);
        }
    }//GEN-LAST:event_btnBuscarVentasActionPerformed
    private void listarVentas(Date finicio, Date ffin){
        ResultSet rsMarcas = null;
        DefaultTableModel model = new DefaultTableModel();
        try {
            model.addColumn("Codigo");
            model.addColumn("Fecha");
            model.addColumn("Cliente");
            model.addColumn("Subtotal");
            model.addColumn("Igv");
            model.addColumn("Total");
            model.addColumn("Estado de pago");
            model.addColumn("Tipo pago");
            model.addColumn("Tipo comprobante");
            
            clsVenta objVenta = new clsVenta();
            rsMarcas = objVenta.listarVentaPorIntervalo(finicio, ffin);
            while (rsMarcas.next()){
                String tipoc = (rsMarcas.getBoolean("tipocomprobante")) ? "Boleta":"Factura" ;
                String tipop = (rsMarcas.getBoolean("tipopago"))? "Contado":"Factura";
                model.addRow(new Object[]{rsMarcas.getInt("numventa"), 
                    rsMarcas.getString("fecha"),
                    rsMarcas.getString("cliente"),
                    rsMarcas.getFloat("subtotal"),
                    rsMarcas.getFloat("igv"),
                    rsMarcas.getFloat("total"),
                    rsMarcas.getBoolean("estadopago"),
                    tipop,
                    tipoc
                } );
            }
            tblVentas.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error", "Error al listar tabla", WIDTH);
        }
        
    }
    
