    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        // TODO add your handling code here:
        try {
            if(btnNuevo.getText().equals("NUEVO")){
                btnNuevo.setText("GUARDAR");
                limpiarControles();
                txtCodigo.setText(String.valueOf(objUsuario.generarCodigoUsuario()));
                txtUsuario.requestFocus();
            }else{
                if (txtContraseña.getText().equals(txtCambiarContraseña.getText())){
                    btnNuevo.setText("NUEVO");
                    objUsuario.registrar(Integer.parseInt(txtCodigo.getText()), txtUsuario.getText(), txtContraseña.getText(), txtNombre.getText(), txtCargo.getText(), chkVigencia.isSelected(), txtPregunta.getText(), txtRespuesta.getText());
                    limpiarControles();
                    listarUsuarios();
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Las contraseñas no coinciden");
                    txtContraseña.requestFocus();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

