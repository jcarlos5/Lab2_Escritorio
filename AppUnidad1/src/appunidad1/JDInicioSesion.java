    public void iniciarSesion(){
        if(txtUsuario.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar el nombre de Usuario", "ALERTA", JOptionPane.WARNING_MESSAGE);
            txtUsuario.requestFocus();
        }else if(txtContraseña.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar su contraseña", "ALERTA", JOptionPane.WARNING_MESSAGE);
            txtContraseña.requestFocus();
        }else{
            try {
                if(objUsuario.validarExistencia(txtUsuario.getText())){
                    if(objUsuario.validarVigencia(txtUsuario.getText())){
                        nombreUsuario = objUsuario.login(txtUsuario.getText(), txtContraseña.getText());
                        if(nombreUsuario.equals("")){
                            numIntentos ++;
                            if(numIntentos > 3){
                                JOptionPane.showMessageDialog(null,"Superó los tres intentos, responda la pregunta secreta", "ERROR", JOptionPane.PLAIN_MESSAGE);
                                responderPregunta();
                            }else{
                                JOptionPane.showMessageDialog(rootPane, "Datos incorrectos", "ERROR", JOptionPane.ERROR_MESSAGE);
                            }
                        }else{
                            JOptionPane.showMessageDialog(rootPane, "Bienvenido "+nombreUsuario);
                            sesion = true;
                            objUsuario.registrarInicio(txtUsuario.getText());
                            dispose();
                        }
                    }else{
                        JOptionPane.showMessageDialog(rootPane, "El usuario no está vigente", "ERROR", JOptionPane.ERROR_MESSAGE);
                    }
                }else{
                    JOptionPane.showMessageDialog(rootPane, "El usuario no está Existe", "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    
    void responderPregunta(){
        try {
            pregunta = objUsuario.obtenerPreguntaSecreta(txtUsuario.getText());
            String rpta = JOptionPane.showInputDialog(rootPane, pregunta, "Pregunta Secreta", JOptionPane.PLAIN_MESSAGE);
            if (rpta != null){
                nombreUsuario = objUsuario.validarPreguntaSecreta(txtUsuario.getText(), rpta);
                if (nombreUsuario.equals("")){
                    JOptionPane.showMessageDialog(null,"Acceso incorrecto, intente nuevamente!!" , "ERROR", JOptionPane.ERROR_MESSAGE);
                    responderPregunta();
                }else{
                    JOptionPane.showMessageDialog(null,nombreUsuario + " Bienvenido al Sistema \nPor motivos de seguridad debes actualizar tu Contraseña", "SISTEMA", JOptionPane.PLAIN_MESSAGE);
                    sesion = true;
                    objUsuario.registrarInicio(txtUsuario.getText());
                    cambiarContraseña(true);
                    this.dispose();
                } 
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }
     
    void cambiarContraseña(boolean need){
        try {
            if (nombreUsuario.equals("")){
                String clave;
                if (txtContraseña.getText().equals("")){
                    clave = JOptionPane.showInputDialog(rootPane, "Necesitamos verificar que eres tú \nEscribe tu Contraseña Actual", "Cambiar contraseña", JOptionPane.PLAIN_MESSAGE);
                }else{
                    clave = txtContraseña.getText();
                }
                if (clave != null){
                    nombreUsuario = objUsuario.login(txtUsuario.getText(), clave);
                    if(nombreUsuario.equals("")){
                        JOptionPane.showMessageDialog(rootPane, "La contraseña es incorrecta", "Error", JOptionPane.ERROR_MESSAGE);
                        txtContraseña.requestFocus();
                    }else{
                        cambiarContraseña(need);
                    }
                }
            }else{
                JDCambiarContraseña objContraseña = new JDCambiarContraseña((Frame) SwingUtilities.getWindowAncestor(this), rootPaneCheckingEnabled);
                objContraseña.setLocationRelativeTo(null);
                objContraseña.setUser(txtUsuario.getText());
                objContraseña.setNecesario(need);
                objContraseña.setVisible(true);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    
