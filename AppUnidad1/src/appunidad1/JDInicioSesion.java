    private void txtUsuarioFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtUsuarioFocusLost
        // TODO add your handling code here:
        try {
            if(txtUsuario.getText().length()>0){
                if(objUsuario.validarExistencia(txtUsuario.getText())){
                    String ultimoInicio[] = objUsuario.ultimaSesion(txtUsuario.getText());
                    if (ultimoInicio != null){
                        lblUltimaSesion.setText("Útlima vez: " + ultimoInicio[0] + " a las " + ultimoInicio[1]);
                    }else{
                        lblUltimaSesion.setText("Útlima vez: " + "Nunca");
                    }
                }else{
                    lblUltimaSesion.setText("El usuario no existe");
                }
            }else{
                lblUltimaSesion.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_txtUsuarioFocusLost

