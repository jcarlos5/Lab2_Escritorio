    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        // TODO add your handling code here:
        String documento;
        if(String.valueOf(tblClientes.getValueAt(tblClientes.getSelectedRow(), 0))!="null"){
            documento=tblClientes.getValueAt(tblClientes.getSelectedRow(), 0).toString();
        }else{
            documento=tblClientes.getValueAt(tblClientes.getSelectedRow(), 1).toString();
        }
        txtDocumento.setText(documento);
        listarClientes();
        listarDeudas();
        txtDocumentoActionPerformed(null);
    }//GEN-LAST:event_tblClientesMouseClicked
    private void listarDeudas(){
        
        documento=txtDocumento.getText();
        ResultSet rscuota = null;
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Venta");
        modelo.addColumn("NumCuota");
        modelo.addColumn("Monto");
        modelo.addColumn("Fecha");
        modelo.addColumn("Estado");
        
        try {
            rscuota=objcuota.listarcuotasporpagar(documento);
            while(rscuota.next()){
                modelo.addRow(new Object[]{rscuota.getInt("numVenta"),rscuota.getInt("numcuota"),rscuota.getFloat("monto"), rscuota.getString("fecha"), rscuota.getBoolean("Estado")});
           }
        } catch (Exception e) {
           JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
        tblCuotas.setModel(modelo);
        
    }
    
    private void datos(){
        documento=txtDocumento.getText();
        ResultSet rs=null;
        
        try {
            rs=objcuota.datoscliente(documento);
            while(rs.next()){
                txtNombre.setText(rs.getString("nombres"));
                txtNumeroVenta.setText(rs.getString("numVenta"));
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }
    
    private void limpiarControles(){
        txtMonto.setText("");
        txtMontoIngresado.setText("");
        txtVuelto.setText("");
    }
    
    private void listarClientes(){
        
        ResultSet rsClientes = null;
        try {
            rsClientes = objCliente.filtrarClientes(txtDocumento.getText());
            DefaultTableModel modelo = new DefaultTableModel();
            
            modelo.addColumn("DNI");
            modelo.addColumn("RUC");
            modelo.addColumn("NOMBRE");
            
            while(rsClientes.next()){
                modelo.addRow(new Object[]{rsClientes.getString("dni"), rsClientes.getString("ruc"), rsClientes.getString("nombres")});
            }
            tblClientes.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }
