    private void tblVentasCreditoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblVentasCreditoMouseClicked
        try {                                              
            clsVenta objVenta = new clsVenta();
            boolean flag  = objVenta.validarCambioProducto( Integer.valueOf(String.valueOf(tblVentasCredito.getValueAt(tblVentasCredito.getSelectedRow(), 0))) );
            if (flag){
                listarDetalle(Integer.valueOf(String.valueOf(tblVentasCredito.getValueAt(tblVentasCredito.getSelectedRow(), 0))));
                txtVenta.setText( String.valueOf(tblVentasCredito.getValueAt(tblVentasCredito.getSelectedRow(), 0)) );
                txtTotal.setText(String.valueOf(tblVentasCredito.getValueAt(tblVentasCredito.getSelectedRow(), 4)));
                txtSubTotal.setText(String.valueOf(tblVentasCredito.getValueAt(tblVentasCredito.getSelectedRow(), 3)));
                txtIgv.setText(String.valueOf(tblVentasCredito.getValueAt(tblVentasCredito.getSelectedRow(), 2)));
            }else {
                JOptionPane.showMessageDialog(this, "No puede devolver en esta venta porque ya tiene un pago registrado");
                this.dispose();
            }
        } catch (Exception ex) {
            Logger.getLogger(JDCambioProducto.class.getName()).log(Level.SEVERE, null,ex);
        }
    }//GEN-LAST:event_tblVentasCreditoMouseClicked

