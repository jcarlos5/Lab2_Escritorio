    public void calcularTotal(){
        try{
            int subTotal=0;
            for(int i=0; i<tblDetalle.getRowCount(); i++){
                subTotal += Float.parseFloat(String.valueOf(tblDetalle.getValueAt(i, 5)));
            }
            float igv = (float) (subTotal * 0.18);
            txtIgv.setText(String.valueOf(igv));
            txtSubTotal.setText(String.valueOf(subTotal));
            txtTotal.setText(String.valueOf(subTotal+igv));
        }catch(Exception e){
        }
    }
    
    
    public void agregarProducto(int producto, int cantidad, int descuento, int fila){
        if(producto!=0 && cantidad!=0){
            ResultSet rs = null;
            try {
                tblDetalle.setModel(modelo);
                modelo.removeRow(fila);
                
                int stock = objProducto.getStock(producto);
                new_producto =  producto;
                if(cantidad > stock){
                    cantidad = stock;
                    JOptionPane.showMessageDialog(rootPane, "Stock Insuficiente");
                }
                
                rs = objProducto.buscarProducto(producto);
                
                while(rs.next()){
                    modelo.addRow(new Object[]{rs.getString("codproducto"), 
                        rs.getString("nomproducto"), 
                        cantidad, 
                        rs.getString("precio"), 
                        descuento+"%", 
                        (cantidad * (rs.getFloat("precio")-descuento*rs.getFloat("precio")/100))});
                }
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, e.getMessage());
            }
        }
        calcularTotal();
    }
    
