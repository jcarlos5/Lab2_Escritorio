    public void setDatos(Integer numVenta, String cliente, String doc, Float sub, Float igv,Float monto,Float vuelto, Boolean pagado, Boolean tipoc ){
        txtDocumento.setText(doc);
        txtNombreC.setText(cliente);
        txtNumeroVenta.setText(String.valueOf(numVenta));
        txtMonto.setText(String.valueOf(monto));
        txtSubtotal.setText(String.valueOf(sub));
        txtIgv.setText(String.valueOf(igv));
        txtVuelto.setText(String.valueOf(vuelto));
        if (pagado){
            txtEstado.setText("Pagado");
        }else {
            txtEstado.setText("No pagado");
        }
        if (tipoc){
            txtTipoComprobante.setText("Boleta");
        }else{
            txtTipoComprobante.setText("Factura");
        }        
    }
    
    

