    public void setData(int cod) throws Exception{
        ResultSet rs = null;
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Nomb. Producto");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Precio venta");
        modelo.addColumn("Descuento");
        modelo.addColumn("Subtotal");
        
        clsVenta objVenta = new clsVenta();
        rs = objVenta.generarComprobante(cod);
        while (rs.next()){
            lblFecha.setText(String.valueOf(rs.getDate("fecha")));
            lblDireccion.setText(rs.getString("direccion"));
            String lote =  rs.getString("lote");
            String numero =  rs.getString("numero");
            lblNumeroComprobante.setText(lote + '-'+numero);
            boolean tipoc = rs.getBoolean("tipocomprobante");
            if (tipoc){
                lblDocumento.setText(rs.getString("dni"));
                txtTipoCom.setText("Boleta");
            }else {
                lblDocumento.setText(rs.getString("ruc"));
                txtTipoCom.setText("Factura");
            }
            lblNombreC.setText(rs.getString("cliente"));
            lblTotal.setText(String.valueOf(rs.getFloat("totalv")));
            lblSub.setText(String.valueOf(rs.getFloat("subtotalv")));
            lblIgv.setText(String.valueOf(rs.getFloat("igv")));

            modelo.addRow(new Object[]{
                rs.getString("nomproducto"), 
                rs.getInt("cantidad"),
                rs.getFloat("precioventa"),
                rs.getInt("descuento"),
                rs.getFloat("dsubtotal")
            });
        }
        
        tblDetalle.setModel(modelo);
    }
    
    
