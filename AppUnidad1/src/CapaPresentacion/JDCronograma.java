    public void listarCreditos(int venta){
        ResultSet rs;
        DefaultTableModel model = new DefaultTableModel();
        clsCuota objCuota = new clsCuota();
        try {
            model.addColumn("Numero de cuota");
            model.addColumn("Fecha programada");
            model.addColumn("Fecha pago");
            model.addColumn("Estado pago");
            model.addColumn("Monto");
        
            rs = objCuota.cuotasPorVenta(venta);
            while (rs.next()){
                String cancel = (rs.getBoolean("cancelada"))? "Pagado":"No pagado" ;
                model.addRow(new Object[]{rs.getInt("numcuota"), 
                    rs.getDate("fechaprogramada"),
                    rs.getDate("fechapago"),
                    cancel,
                    rs.getFloat("monto")
                } );
            }
            tblCronograma.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error", "Error al listar tabla", WIDTH);
        }
        
    }
