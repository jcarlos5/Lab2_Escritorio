    public void setVenta(String num){
        showVenta(num);
    }
    
    private void showVenta(String num){
        clsDocumentoVenta obj = new clsDocumentoVenta();
        try {
            ResultSet rs = obj.getDatos(Integer.parseInt(num));
            
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.addColumn("Cantidad");
            modelo.addColumn("Descripci√≥n");
            modelo.addColumn("Prec. Unit.");
            modelo.addColumn("Descuento");
            modelo.addColumn("Subtotal");
            Formatter frmt = new Formatter();
            
            while (rs.next()){
                lblnumBoleta.setText("B-001-"+String.valueOf(frmt.format("%05d",rs.getInt(1))));
                lblCliente.setText(rs.getString(3));
                lblFecha.setText(rs.getString(2));
                lblDocumento.setText(rs.getString(4));
                lblsubtotal.setText(rs.getString(7));
                lblIgv.setText(rs.getString(8));
                lblMonto.setText(rs.getString(6));
                modelo.addRow(new Object[]{rs.getString(10), rs.getString(11), rs.getString(12), rs.getString(13), rs.getString(14)});
            }
            tblDetalle.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }
    
