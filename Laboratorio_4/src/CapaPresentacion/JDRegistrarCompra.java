    private void listarProveedor(){
        try {
            ResultSet rs = null;
            rs = objPro.listarProveedores();
            DefaultTableModel modeloPro = new DefaultTableModel();
                modeloPro.addColumn("CÓDIGO");
                modeloPro.addColumn("RUC");
                modeloPro.addColumn("NOMBRE");
                while (rs.next()) {
                    modeloPro.addRow(new Object [] {rs.getInt("codProveedor"),rs.getString("ruc"), rs.getString("nombre")});
                }
            tblProveedor.setModel(modeloPro);
        } catch (Exception ex) {
            Logger.getLogger(JDRegistrarCompra.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    public void agregarProducto(int cod_producto, int cantidad, float precio){
        if(cod_producto!=0 && cantidad!=0){
            ResultSet rsP = null;
            boolean repetido = false;
            int fila = -1;
            try {
                tblProductos.setModel(modeloP);
                //Valido si ya existe el producto en la tabla
                for (int i=0; i<tblProductos.getRowCount(); i++){
                    if(Integer.parseInt(String.valueOf(tblProductos.getValueAt(i, 0))) == cod_producto){
                        repetido = true;
                        fila = i;
                    }
                }
                //Añadir el producto a la tabla                
                rsP = objP.buscarProducto(cod_producto);
                    while(rsP.next()){
                        modeloP.addRow(new Object[]{rsP.getString("codproducto"),rsP.getString("nomproducto"),precio,cantidad,(cantidad * precio)});
                    }
                btnQuitar.setEnabled(true);
                btnModificar.setEnabled(true);
                btnGuardar.setEnabled(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, e.getMessage());
            }
        }
        calcularTotal();
    }
    
    public void calcularTotal(){
        try{
            subTotal=0;
            for(int i=0; i<tblProductos.getRowCount(); i++){
                subTotal += Float.parseFloat(String.valueOf(tblProductos.getValueAt(i, 4)));
            }
            float igv = (float) (subTotal * 0.18);            
            txtSubTotal.setText(String.valueOf(subTotal-igv));
            txtIGV.setText(String.valueOf(igv));
            txtTotal.setText(String.valueOf(subTotal));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }
    
