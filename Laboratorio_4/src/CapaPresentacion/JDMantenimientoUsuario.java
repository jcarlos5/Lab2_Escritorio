    private void limpiarControles(){
        txtCodigo.setText("");
        txtUsuario.setText("");
        txtNombre.setText("");
        cmbCargo.setSelectedIndex(0);
        chkVigencia.setSelected(false);
        deshabilitarEd();
    }
    
    private void listarUsuarios(){
        ResultSet rsUsuarios = null;
        DefaultTableModel modelo = new DefaultTableModel(){
                                            @Override
                                            public boolean isCellEditable(int row, int col)
                                            {
                                                return false;
                                            }
                                            };
        modelo.addColumn("CÓDIGO");
        modelo.addColumn("USUARIO");
        modelo.addColumn("NOMBRE");
        modelo.addColumn("CARGO");
        modelo.addColumn("ESTADO");
        modelo.addColumn("PREGUNTA");
        try {
            rsUsuarios=objUsuario.listarUsuarios();
            while(rsUsuarios.next()){
                modelo.addRow(new Object[]{rsUsuarios.getInt("codusuario"),rsUsuarios.getString("nomusuario"), rsUsuarios.getString("nombrecompleto"), rsUsuarios.getString("cargo"), rsUsuarios.getBoolean("estado")?"Vigente":"No Vigente", rsUsuarios.getString("pregunta")});
           }
        } catch (Exception e) {
           JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
        tblUsuarios.setModel(modelo);
    }
    
