    public void modoPerfil(String usuario){
        contenedorTabla.setVisible(false);
        btnContraseña.setText("Cambiar Contraseña");
        btnNuevo.setVisible(false);
        btnModificar.setText("Guardar");
        btnModificar.setEnabled(true);
        lblCargo.setVisible(false);
        cmbCargo.setVisible(false);
        
        btnEliminar.setVisible(false);
        btnDardeBaja.setVisible(false);
        btnContraseña.setEnabled(true);
        btnPregunta.setEnabled(true);
        btnLimpiar.setVisible(false);
        
        btnBuscar.setVisible(false);
        txtCodigo.setEditable(false);
        lblEstado.setVisible(false);
        chkVigencia.setVisible(false);
        
        modoPerfil = true;
        
        btnModificar.setLocation(btnNuevo.getLocation());
        btnSalir.setLocation(btnModificar.getX(), btnModificar.getY()+btnModificar.getHeight()+10);
        
        user = usuario;
        obtenerDatos();
    }
    
    private void obtenerDatos(){
        try {
            ResultSet rs = objUsuario.getDatos(user);
            if (rs.next()){
                txtCodigo.setText(rs.getString("codusuario"));
                txtNombre.setText(rs.getString("nombrecompleto"));
                cmbCargo.setSelectedItem(rs.getString("cargo"));
                txtUsuario.setText(rs.getString("nomusuario"));
                chkVigencia.setSelected(rs.getBoolean("estado"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }
    
    public int getSizePerfil(){
        return contenedor.getHeight()+60;
    }
    
    private boolean validarDatos(){
        boolean valido = false;
        if(txtCodigo.getText().replace(" ", "").length()!=0 && txtNombre.getText().replace(" ", "").length()!=0 && txtUsuario.getText().replace(" ", "").length()!=0){
            valido = true;
        }
        return valido;
    }
    
