    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        ResultSet rsUsuario = null;
        try {
            if (txtCodigo.getText().equals("")){
                JOptionPane.showMessageDialog(this,"Debe ingresar un código para buscar");
            }else{
                rsUsuario = objUsuario.buscarUsuario(Integer.parseInt( txtCodigo.getText()));
                if (rsUsuario.next()){
                        txtUsuario.setText(rsUsuario.getString("nomusuario"));
                        txtNombre.setText(rsUsuario.getString("nombrecompleto"));
                        cmbCargo.setSelectedItem(rsUsuario.getString("cargo"));
                        chkVigencia.setSelected(rsUsuario.getBoolean("estado"));
                        rsUsuario.close();
                        habilitarEd();
                        btnNuevo.setText("NUEVO");
                        if(txtUsuario.getText().equals(user)){
                            btnPregunta.setEnabled(true);
                        }else{
                            btnPregunta.setEnabled(false);
                        }
                        modificando = true;
                }else{
                    JOptionPane.showMessageDialog(this,"Código de usuario no existe!");
                    modificando = false;
                    limpiarControles();
                }
            }   
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
            txtCodigo.setText("");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

